package gui_pt.drawObjects;

public class OutputValue implements DetailValue {
	
	private DrawNode dn;
	
	//################################################################################################
	// Constructor
	//################################################################################################
	
	public OutputValue(DrawNode dn){
		
		this.dn = dn;
	}
	
	//################################################################################################
	// Methods
	//################################################################################################
	
	//################################################################################################
	// DetailValue
	//################################################################################################

	@Override
	public String detailToString() {
		
		double output = dn
				.getAccNode()
				.fire(dn
						.getDrawTree()
						.getTcp()
						.getProtoInstance());
		
		return Double.toString((int)(output*1000)/1000d);
	}
	
	//################################################################################################
	// GET and SET
	//################################################################################################


}
