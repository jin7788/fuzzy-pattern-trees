package gui_pt.drawObjects;

public class StaticValue implements DetailValue{
	
	private String value;
	
	//################################################################################################
	// Constructor
	//################################################################################################
	
	//################################################################################################
	// Methods
	//################################################################################################
	
	//################################################################################################
	// DetailValue
	//################################################################################################

	@Override
	public String detailToString() {
		
		return value;
	}
	
	//################################################################################################
	// GET and SET
	//################################################################################################

	public String getValue() {
		return value;
	}

	public void setValue(String value) {
		this.value = value;
	}
	

}
